    private void verifyDefaultUser() {
        IAdmin admin = BusinessFactory.getInstance().getAdmin();
        AdminVO vo = new AdminVO(java.util.ResourceBundle.getBundle("presentation/desktop/languages/Bundle").getString("ADMIN"), java.util.ResourceBundle.getBundle("presentation/desktop/languages/Bundle").getString("ADMIN"));
        try {
            if(admin.getAdminByLogin(java.util.ResourceBundle.getBundle("presentation/desktop/languages/Bundle").getString("ADMIN")) == null){
                admin.create(vo);
                
            }
        } catch (BusinessException ex) {
            Logger.getLogger(LoginForm.class.getName()).log(Level.SEVERE, null, ex);
        }
       
    }
