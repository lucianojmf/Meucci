    private boolean authenticate() {
        boolean toReturn = false;

        //Recupera dos valores digitados nos campos de login e senha
        String login = txtName.getText();
        String password = String.copyValueOf(txtPassword.getPassword());

        //verifica se os campos digitados são diferentes de nulo
        if (!login.isEmpty() && !password.isEmpty()) {
            //Valida os dados do usuario
            IAdmin admin = BusinessFactory.getInstance().getAdmin();
            AdminVO vo = new AdminVO(login, password);

            try {
                toReturn = admin.authenticate(vo);
            } catch (Exception e) {
                toReturn = false;
            }
        }
        return toReturn;
    }
